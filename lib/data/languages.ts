export const getLanguageByCode = (code: string): Language | undefined => {
  return LANGUAGES.find(lang => lang.code === code);
};

export const getLanguageByName = (name: string): Language | undefined => {
  return LANGUAGES.find(lang => 
    lang.name.toLowerCase() === name.toLowerCase() || 
    lang.nativeName.toLowerCase() === name.toLowerCase()
  );
};

export const getProficiencyLevelLabel = (level: ProficiencyLevel): string => {
  return PROFICIENCY_LEVELS.find(p => p.value === level)?.label || level;
};

export const getProficiencyLevelDescription = (level: ProficiencyLevel): string => {
  return PROFICIENCY_LEVELS.find(p => p.value === level)?.description || '';
}; 